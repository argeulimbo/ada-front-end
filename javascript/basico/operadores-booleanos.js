// Operadores Booleanos: Comparações

const numero1 = 10;
const numero2 = 12;

console.log(numero1 == numero2); // false
console.log(numero1 === numero2); // false

// Comparação de valor usando 3 sinais compara valor e tipo
// Comparação de valor usando 2 sinais compara apenas valor

console.log(numero1 !== "10"); // true
console.log(numero1 != "10"); // false